n = int(input())
r = [''] * 4
if n == 1:
    r = ['1', '9', '0', '-72']
elif n == 2:
    r = ['50', '19', '25', '-63']
elif n == 3:
    r = ['950', '109', '844', '27']
else:
    r[0] = '9' * (n - 2) + '50'
    r[1] = '1' + '0' * (n - 2) + '9'
    r[3] = '9' * (n - 3) + '27'
    x = 81 * (n - 2) + 75
    l = len(str(x))
    r[2] = '9' * (n - l - 1) + str(10 ** (l + 1) - x)
print('\n'.join(r))
